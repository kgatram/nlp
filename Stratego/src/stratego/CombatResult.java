package stratego;

/**
 * Enumerator class having possible result {WIN, LOSE, DRAW} of a game or attack.
 *
 * @author 220025456
 * @
 */


public enum CombatResult {
    /**
     * result = win.
     */
    WIN,
    /**
     * result = draw.
     */
    DRAW,
    /**
     * result = lose.
     */
    LOSE
}
